// 根据年份返回 ECharts 的 data 数组
function getDataByYear(year) {
    const levels = yearData[year];
    if (!levels) return [];
    const result = [];
    for (const [level, provinces] of Object.entries(levels)) {
        const val = levelValue[level];
        for (let prov of provinces) {
            let mappedName = nameMap[prov] || prov;
            result.push({ name: mappedName, value: val });
        }
    }
    return result;
}